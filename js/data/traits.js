/**
 * 세부특성 풀 — NPC/플레이어에게 랜덤 배정되는 성격 태그
 */
export const TRAITS = [
  // 생활 습관
  '요리잘함', '운동좋아함', '술좋아함', '집순이', '파티광',
  '여행좋아함', '패션감각', '자기관리', '결벽증',
  // 성격/가치관
  '감성적', '계산적', '순수함', '자유분방함', '계획적',
  '즉흥적', '무뚝뚝', '도도함', '카리스마', '백치미',
  // 상태/이력
  '고학력', '저학력', '워커홀릭', '무직', '자녀있음',
  '반려동물', '종교적', '저축형', '소비형', '바람끼',
  '가정적', '완벽주의', '해외이주계획', '편지좋아함',
];

/**
 * 특정 개수만큼 랜덤 특성 뽑기 (중복 없음)
 */
export function pickRandomTraits(count) {
  const shuffled = [...TRAITS].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
}
